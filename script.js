function getConcern() {
  const selected = document.querySelector('input[name="category"]:checked');
  const output = document.getElementById("output");

  if (!selected) {
    output.textContent = "고민 종류를 선택해주세요!";
    return;
  }

  const category = selected.value;
  const arr = concerns[category];
  if (!arr || arr.length === 0) {
    output.textContent = "해당 카테고리에 고민이 없습니다.";
    return;
  }

  const randomIdx = Math.floor(Math.random() * arr.length);
  output.textContent = arr[randomIdx];
}
